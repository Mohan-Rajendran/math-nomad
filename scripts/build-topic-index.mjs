import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const articlesRoot = path.join(projectRoot, "writing", "articles");
const topicsRoot = path.join(projectRoot, "writing", "topics");

function unquote(value) {
  const text = value.trim();
  if (
    (text.startsWith('"') && text.endsWith('"')) ||
    (text.startsWith("'") && text.endsWith("'"))
  ) {
    return text.slice(1, -1);
  }
  return text;
}

function parseInlineList(value) {
  const inner = value.trim().slice(1, -1);
  if (!inner.trim()) return [];
  return inner.split(",").map(unquote);
}

function readFrontMatter(source) {
  const lines = source.replaceAll("\r\n", "\n").split("\n");
  if (lines[0]?.trim() !== "---") return null;

  const closing = lines.findIndex((line, index) => index > 0 && line.trim() === "---");
  if (closing < 0) return null;

  const metadata = {};
  let activeList = null;

  for (const line of lines.slice(1, closing)) {
    const field = line.match(/^([A-Za-z0-9_-]+):(?:\s*(.*))?$/);
    if (field) {
      const [, key, rawValue = ""] = field;
      const value = rawValue.trim();
      activeList = null;

      if (!value) {
        metadata[key] = [];
        activeList = key;
      } else if (value.startsWith("[") && value.endsWith("]")) {
        metadata[key] = parseInlineList(value);
      } else {
        metadata[key] = unquote(value);
      }
      continue;
    }

    const listItem = line.match(/^\s+-\s+(.+)$/);
    if (listItem && activeList) {
      metadata[activeList].push(unquote(listItem[1]));
    }
  }

  return metadata;
}

async function findArticleSources(directory) {
  const entries = await readdir(directory, { withFileTypes: true });
  const files = [];

  for (const entry of entries) {
    const absolute = path.join(directory, entry.name);
    if (entry.isDirectory()) {
      files.push(...(await findArticleSources(absolute)));
    } else if (entry.isFile() && entry.name.endsWith(".qmd")) {
      files.push(absolute);
    }
  }

  return files;
}

function articleHref(sourcePath) {
  const relative = path.relative(articlesRoot, sourcePath).split(path.sep).join("/");
  if (relative.endsWith("/index.qmd")) {
    return `/writing/articles/${relative.slice(0, -"index.qmd".length)}`;
  }
  return `/writing/articles/${relative.replace(/\.qmd$/, ".html")}`;
}

const topicDefinitions = JSON.parse(
  await readFile(path.join(topicsRoot, "topics.json"), "utf8"),
);
const articleSources = await findArticleSources(articlesRoot);
const articles = [];

for (const sourcePath of articleSources) {
  const metadata = readFrontMatter(await readFile(sourcePath, "utf8"));
  if (!metadata?.title) continue;

  articles.push({
    title: metadata.title,
    description: metadata.description ?? "",
    date: metadata.date ?? "",
    categories: Array.isArray(metadata.categories) ? metadata.categories : [],
    href: articleHref(sourcePath),
  });
}

for (const topic of topicDefinitions) {
  const matchingArticles = articles
    .filter((article) => article.categories.includes(topic.category))
    .sort((left, right) => String(right.date).localeCompare(String(left.date)))
    .map(({ categories, ...article }) => article);

  const destination = path.join(topicsRoot, topic.slug, "articles.yml");
  const contents = [
    "# Generated by scripts/build-topic-index.mjs. Do not edit by hand.",
    JSON.stringify(matchingArticles, null, 2),
    "",
  ].join("\n");
  await writeFile(destination, contents, "utf8");
}
